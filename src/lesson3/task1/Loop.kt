 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var count = 1
    var number = 1
    while (count < n) {
        number++
        var proizveden = number * number
        while (proizveden > 0) {
            proizveden /= 10
            count++
        }
    }
    var proizveden = number*number
    for (i in 1..count-n) {
        proizveden /= 10
    }
    return proizveden % 10
}
